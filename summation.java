/*PARAMETER:    The number will always be a positive integer greater than 0
*RESULT:        A method that finds the summantion of every number
*               from 1 to num.
*EXAMPLE:       2 -> 3 (1 + 2)
*/

public class GrassHopper {

    public static int summation(int n) {

       return  n*(n+1)/2;
    }
}